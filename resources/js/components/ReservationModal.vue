<script setup lang="ts">
import { ref, computed } from 'vue'
import { Dialog, DialogContent, DialogHeader, DialogTitle } from '@/components/ui/dialog'
import { Button } from '@/components/ui/button'
import { Card, CardContent, CardDescription, CardHeader, CardTitle } from '@/components/ui/card'

// Props y emits
const props = defineProps<{
  open?: boolean
}>()

const emit = defineEmits<{
  'update:open': [value: boolean]
  'reservation:submit': [data: ReservationData]
}>()

// Estado del modal
const isOpen = computed({
  get: () => props.open || false,
  set: (value) => emit('update:open', value)
})

// Datos de la reserva
const checkInDate = ref('')
const checkOutDate = ref('')
const selectedRoomType = ref('')
// const selectedDate = ref<Date | null>(null) // Removed unused variable
const isSelectingCheckIn = ref(true)

// Tipos de habitación
const roomTypes = [
  {
    id: 'suite-deluxe',
    name: 'Suite Deluxe',
    description: 'Habitación amplia con vista al mar',
    price: 95,
    image: '/images/suite-deluxe.jpg'
  },
  {
    id: 'habitacion-estandar',
    name: 'Habitación Estándar',
    description: 'Cómoda habitación con todas las comodidades',
    price: 65,
    image: '/images/habitacion-estandar.jpg'
  },
  {
    id: 'suite-familiar',
    name: 'Suite Familiar',
    description: 'Perfecta para familias hasta 4 personas',
    price: 120,
    image: '/images/suite-familiar.jpg'
  }
]

// Días de la semana
const weekDays = ['Dom', 'Lun', 'Mar', 'Mie', 'Jue', 'Vie', 'Sab']

// Calcular fechas para el calendario
const generateCalendarDates = () => {
  const today = new Date()
  const dates = []
  
  // Generar 35 días (5 semanas)
  for (let i = 0; i < 35; i++) {
    const date = new Date(today)
    date.setDate(today.getDate() + i)
    dates.push(date)
  }
  
  return dates
}

const calendarDates = ref(generateCalendarDates())

// Función para verificar si una fecha está seleccionada
const isDateSelected = (date: Date) => {
  const dateStr = formatDate(date)
  return checkInDate.value === dateStr || checkOutDate.value === dateStr
}

// Función para verificar si una fecha está en el rango seleccionado
const isDateInRange = (date: Date) => {
  if (!checkInDate.value || !checkOutDate.value) return false
  
  // const dateStr = formatDate(date) // Removed unused variable
  const checkIn = new Date(checkInDate.value)
  const checkOut = new Date(checkOutDate.value)
  const currentDate = new Date(date)
  
  return currentDate > checkIn && currentDate < checkOut
}

// Función para verificar si una fecha está disponible
const isDateAvailable = (date: Date) => {
  const today = new Date()
  today.setHours(0, 0, 0, 0)
  return date >= today
}

// Función para seleccionar fecha del calendario
const selectCalendarDate = (date: Date) => {
  if (!isDateAvailable(date)) return
  
  const dateStr = formatDate(date)
  
  if (isSelectingCheckIn.value) {
    checkInDate.value = dateStr
    isSelectingCheckIn.value = false
  } else {
    if (new Date(dateStr) > new Date(checkInDate.value)) {
      checkOutDate.value = dateStr
      isSelectingCheckIn.value = true
    } else {
      // Si la fecha de salida es anterior a la de entrada, intercambiar
      checkOutDate.value = checkInDate.value
      checkInDate.value = dateStr
    }
  }
}

// Función para formatear fecha
const formatDate = (date: Date) => {
  return date.toLocaleDateString('es-ES', {
    day: '2-digit',
    month: '2-digit',
    year: 'numeric'
  })
}

